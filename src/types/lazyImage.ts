/**
 * Интерфейс пропсов для компонента LazyImage
 */
export interface LazyImageProps {
  /** URL изображения */
  src: string;
  /** Альтернативный текст для изображения (важно для доступности) */
  alt: string;
  /** URL заглушки, которая будет показана, если основное изображение не загрузится */
  fallbackSrc?: string;
  /** Дополнительные CSS-классы для изображения */
  className?: string;
  /** Дополнительные CSS-классы для контейнера изображения */
  containerClassName?: string;
  /** Режим отображения изображения */
  objectFit?: 'cover' | 'contain' | 'fill' | 'scale-down';
  /** Ширина контейнера */
  width?: string;
  /** Высота контейнера */
  height?: string;
  /** Соотношение сторон (ширина/высота) для предотвращения сдвигов макета */
  aspectRatio?: number;
  /** Текст подписи к изображению (опционально) */
  caption?: string;
} 